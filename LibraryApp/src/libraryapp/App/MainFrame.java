    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        connection = DB.ConnectServer();
        StringBuilder warning = new StringBuilder();
        String bookName = booknameField.getText();
        String id = bookIdField.getText();
        String author = authorField.getText();
        
        
        if(isBookValid(bookName,id,connection)){
            String sql = "INSERT INTO book (bookid,bookname,author) VALUES(?,?,?)";
            try {
                PreparedStatement statement = connection.prepareStatement(sql);
                statement.setString(1, id);
                statement.setString(2, bookName);
                statement.setString(3, author);
                
            
                int rows = statement.executeUpdate();
            
                if (rows > 0){
                    System.out.println("New row inserted");
                }
            
                statement.close();
                connection.close();
            } catch (SQLException ex) {
                Logger.getLogger(LoginFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }else{
            warning.append("Book already exists!");
        }
        
        if(!warning.isEmpty()){
            JOptionPane.showMessageDialog(this, warning.toString(),"Error",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_addButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        connection = DB.ConnectServer();
        StringBuilder warning = new StringBuilder();
        String bookName = booknameField.getText();
        String id = bookIdField.getText();
        
        if(!isBookValid(bookName, id, connection)){
            String sql = "delete from book where bookid = ? or bookname = ?";
                       try {
                PreparedStatement statement = connection.prepareStatement(sql);
                statement.setString(1, id);
                statement.setString(2, bookName);
                
                int rows = statement.executeUpdate();
            
                if (rows > 0){
                    System.out.println("book deleted");
                }
            
                statement.close();
                connection.close();
            } catch (SQLException ex) {
                Logger.getLogger(LoginFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }else{
            warning.append("Book not exists!");
        }
        
        if(!warning.isEmpty()){
            JOptionPane.showMessageDialog(this, warning.toString(),"Error",JOptionPane.WARNING_MESSAGE);
        }
        
        
    }//GEN-LAST:event_deleteButtonActionPerformed

