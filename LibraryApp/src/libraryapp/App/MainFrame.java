    public boolean isBookValid(String bookName,String id,Connection connection){
        boolean condition = true;
        bookName = booknameField.getText();
        id = bookIdField.getText();
        Statement statement;
        
        try {
            String sql = "SELECT bookid,bookname FROM book";
            statement = connection.createStatement();
            ResultSet result = statement.executeQuery(sql);
            int i=0;
            while(result.next()){
                if(bookName.equals(result.getString("bookname")) || id.equals(result.getString("bookid"))){
                    condition = false;
                    return condition;
                }
                i++;
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(LoginFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return condition;
    }
    


